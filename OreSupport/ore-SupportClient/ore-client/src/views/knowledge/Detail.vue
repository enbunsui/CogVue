                  <br />『COPD（慢性閉塞性肺疾患）の患者さんは酸素マスクをしていても、入院2日目頃からボーッとして明らかに認知機能が落ちている』と医療スタッフの方から聞くことがよくあります。臨床研究でも認知機能低下と低酸素血症との相関の報告が多く、注意力の障害が最も共通してあるそうです。また海外の研究（Singhら）でも『COPD患者から有意に多く非健忘型の軽度認知機能障害（MCI）が発症した』と報告されるなど、記憶障害は軽度でも注意障害などの前頭葉機能に関わる認知機能低下が見られるケースが多いようです。
